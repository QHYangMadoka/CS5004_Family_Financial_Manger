package final_project;

/**
 * Enum representing different categories of debt.
 */
public enum DebtCategory {
    MORTGAGE,           // Represents debts related to mortgages.
    VEHICLE_LOAN,       // Represents debts related to vehicle loans.
    CONSUMPTION_LOAN,   // Represents debts related to personal consumption.
    OTHER               // Represents debts that do not fall into the other categories.
}
